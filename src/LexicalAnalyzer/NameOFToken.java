package LexicalAnalyzer;

/* defines a set of named constants that represent the different types of tokens
   recognized by the lexical analyzer during the scanning process. */

public enum NameOFToken {
  IDENTIFIER,
  STRING,
  INTEGER,
  SEMICOLON,
  OPERATOR,
  KEYWORDS,
  DELETE,
  L_PAREN,
  R_PAREN,
  COMMA,
}
